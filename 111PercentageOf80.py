'''
จงเขียนโปรแกรมคำนวน คะแนนร้อยละของคะแนนเต็ม 80
ป้อนตัวเลข 1 จำนวน คือคะแนนที่ได้
ยกตัวอย่างเช่น คะแนน 30 คิดจาก
คะแนนที่ได้ / 80 * 100

In : 30
Out : คะแนนของคุณ : 30.0
คิดเป็นร้อยละ : 37.5%
'''

x = float(input())
print('คะแนนของคุณ :',float(x))
y = round(((x/80)*100),1)
print('คิดเป็นร้อยละ : ',y,'%',sep='')