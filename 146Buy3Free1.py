'''
ร้านขายเครื่องดื่มมีโปรโมชั่น ถ้าดื่มเสร็จสามารถนำฝา 3 ฝาแลกขวดใหม่ได้ 1 ขวด
(ขวดน้ำ 1 ขวดให้ฝา 1 ฝา และ หลังจากแลกแล้วผู้ขายเก็บฝากลับโดยไม่คืนให้ผู้ซื้อ)

In : จำนวนขวดที่ซื้อตอนแรก(n)
Out : จำนวนขวดที่รวมมากสุด

2 > 2
3 > 4
10 > 14
'''

bottle_have = int(input())
cover_have = bottle_have
while cover_have >= 3 :
    if cover_have - 3 >= 0:
        print('3 แลก 1')
        cover_have -= 2     #3แลก 1 ได้้ฝาคืน 1
        bottle_have += 1

print(bottle_have)
