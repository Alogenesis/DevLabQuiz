'''
จงคำนวนพื้นที่ 3 เหลี่ยม
สุตร: 1/2 * ฐาน * สุง
หากได้ผลลัพธ์เป็นทศนิยมให้ตอบเป็นทศนิยม 1 ตำแหน่ง หากไม่ได้ทศนิยม ให้ตอบเป็นจำนวนเต็ม
บรรทัดที่ 1 ความยาวฐาน
บรรทัดที่ 2 ความสูง
Out :
บรรทัดที่ 1 พื้นที่ 3 เหลี่ยม
หากได้ผลลัพธ์เป็นทศนิยมให้ตอบเป็นทศนิยม 1 ตำแหน่ง หากไม่ได้ทศนิยม ให้ตอบเป็นจำนวนเต็ม
'''

base = int(input())
h = int(input())

result = 1/2 * h * base



if result - int(result) == 0:
    print(int(result), 'cm2')
else:
    print(round(result, 1), 'cm2')